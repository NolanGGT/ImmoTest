export function getPrixColor(prix: number): string {
  if (prix >= 15000) return '#800026'
  if (prix >= 13000) return '#bd0026'
  if (prix >= 11000) return '#f03b20'
  if (prix >= 9000)  return '#fd8d3c'
  if (prix >= 7500)  return '#fecc5c'
  return '#ffffb2'
}

export function getPrixLabel(prix: number): string {
  return `${Math.round(prix / 100) * 100} €/m²`
}

export function arrKeyFromCAr(cAr: number): ArrondissementKey {
  return `750${String(cAr).padStart(2, '0')}` as ArrondissementKey
}
